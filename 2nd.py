"""What are Operators?

In programming, operators are symbols or keywords used to perform specific operations on values (known as operands).
Operators are the building blocks of expressions and help perform calculations, 
comparisons, logical decisions, and other operations.

Types of Operators in Python
	1.	Arithmetic Operators
Used to perform basic mathematical operations.
Examples: +, -, *, /, %, //, **
	2.	Relational/Comparison Operators
Used to compare two values and return a Boolean (True or False).
Examples: ==, !=, >, <, >=, <=
	3.	Logical Operators
Used to combine conditional statements.
Examples: and, or, not
	4.	Bitwise Operators
Perform operations at the binary level.
Examples: &, |, ^, ~, <<, >>
	5.	Assignment Operators
Used to assign values to variables.
Examples: =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
	6.	Membership Operators
Used to check if a value exists in a sequence (like lists, strings, tuples).
Examples: in, not in
	7.	Identity Operators
Used to check if two objects are the same in memory.
Examples: is, is not
	8.	Ternary/Conditional Operator
A shorthand for decision-making.
Example: a if condition else b """

a = 5
b = 10
print(a == b)  # Output: False

c = 10
print(b == c)  # Output: True